package com.github.hobbitProg.dcm.unitTests.client.books.selectedBookWindow

import org.scalatest.FreeSpec

/**
  * Verifies window containing information on currently selected book is
  * cleared when requested
  */
class SelectedBookWindowIsClearedWhenRequested
  extends FreeSpec {
  "Given a selected book window" - {
    "and the selected book has a title of Ruins" - {
      "when the selected book is cleared" - {
        "then there is no title for the selected book" in
        pending
      }
    }

    "and the selected book has an author of Kevin J. Anderson" - {
      "when the selected book is cleared" - {
        "then there is no author for the selected book" in
        pending
      }
    }

    "and the selected book has an ISBN of 0061052477" - {
      "when the selected book is cleared" - {
        "then there is no ISBN for the selected book" in
        pending
      }
    }

    "and the selected book has a description of 'Description for Ruins'" - {
      "when the selected book is cleared" - {
        "then there is no description for the selected book" in
        pending
      }
    }

    "and the selected book's cover image is located in Ruins.jpg" - {
      "when the selected book is cleared" - {
        "then there is no cover image for the selected book" in
        pending
      }
    }

    "and the selected book has the following categories: sci-fi,conspiracy" - {
      "when the selected book is cleared" - {
        "then there the selected book has no associated categories" in
        pending
      }
    }
  }
}
